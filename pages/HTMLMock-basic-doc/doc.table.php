<h3 id="header-table">Table</h3>
<p>
    This mode produces a table of a specified number of rows and columns.
    Each cell will contain a single paragraph generated according paragraph min length, paragraph max length, sentence
    min length and sentence max length. The number of sentences being random between paragraph min and max length. The
    number of words in each sentence being a random number between sentence min and max length. It also will optionally
    generate headers for the table if requested.
</p>
<h4>Settings</h4>
<ul>
    <li>Table columns</li>
    <li>Table rows</li>
    <li>Table headers enabled</li>
    <li>Paragraph min length</li>
    <li>Paragraph max length</li>
    <li>Sentence min length</li>
    <li>Sentence max length</li>
</ul>
